package blackjack.cards;

/**
 * Enum for holding card values. Since in blackjack there is no difference
 * between cards with different suits, they are not covered by enum.
 * @author yevhen bilous
 */

public enum Card {
    JACK, QUEEN,
    TWO, THREE, FOUR, FIVE, SIX, SEVEN, EIGHT, NINE, TEN,
    KING, ACE
}
